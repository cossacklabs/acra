// NewCastVal builds new CastVal
func NewCastVal(val Expr, castType []byte) *SQLVal {
	switch v := val.(type) {
	case *SQLVal:
		return &SQLVal{Type: v.Type, Val: v.Val, CastType: castType}
	default:
		return &SQLVal{unknown: val, Type: UnknownVal, CastType: castType}
	}
}

